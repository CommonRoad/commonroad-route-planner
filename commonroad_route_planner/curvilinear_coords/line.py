import numpy as np


class Line:


    def __init__(self,
                 point_1: np.ndarray,
                 point_2: np.ndarray
                 ) -> None:


        self.point_1 = np.ndarray
        self.point_2 = np.ndarray

        self.x_1: float = self.point_1[0]
        self.x_2: float = self.point_2[0]
        self.y_1: float = self.point_1[1]
        self.y_2: float = self.point_2[1]



